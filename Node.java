

public class Node 
{
    public int element;
    
    public Node left;
    public Node right;
    
    public Node(int element)
    {
    	super();
    	this.element = element;
    }

	public int getElement() {
		return element;
	}

	public void setElement(int element) {
		this.element = element;
	}

	public Node getLeft() {
		return left;
	}

	public void setLeft(Node left) {
		this.left = left;
	}

	public Node getRight() {
		return right;
	}

	public void setRight(Node right) {
		this.right = right;
	}
    

  
    
}
